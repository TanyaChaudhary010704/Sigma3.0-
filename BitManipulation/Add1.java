package BitManipulation;
//Adding 1 to the number using BitManipulation
public class Add1 {
  public static void main(String[] args) {
    int n = 5;
    System.out.println(n+"+1 :" + -~n);
    n = 0;
    System.out.println(n+"+1 :" + -~n);
    n = -5;
    System.out.println(n+"+1 :"+ -~n);
    
  }
}
